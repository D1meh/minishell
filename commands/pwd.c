#include "../includes/minishell.h"

int	pwd(void)
{
	char	*l;
	int		i;

	l = malloc(1);
	i = 10;
	while (1)
	{
		l = getcwd(l, i);
		if (l)
			break ;
		i += 10;
	}
	ft_putendl(l);
	free(l);
	return (0);
}
