#include "../includes/minishell.h"

int	env(t_var *v)
{
	if (!v)
		return (1);
	while (v)
	{
		ft_putstr(v->name);
		ft_putstr("=");
		ft_putendl(v->value);
		v = v->next;
	}
	return (0);
}
